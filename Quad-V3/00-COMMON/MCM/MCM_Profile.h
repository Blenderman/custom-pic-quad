#include "System.h"

//==============================================================================
#ifndef __MCMProfile_H
#define __MCMProfile_H
//==============================================================================
// Control Board uses the following pins for Motor Control
//		RP68/RD4    -> OC1 (Motor1)
//		RP69/RD5    -> OC2 (Motor2)
//		RP70/RD6    -> OC3 (Motor3)
//		RP71/RD7    -> OC4 (Motor4)
//------------------------------------------------------------------------------
static inline void _MCM_InitPinMap(void)
    {
    //--------------------------------------------------------------------------
    // Set respective pins as "Output"
    //--------------------------------------------------------------------------
    _TRISD4     = 0;
    _TRISD5     = 0;
    _TRISD6     = 0;
    _TRISD7     = 0;
    //--------------------------------------------------------------------------
    // Reset signal on respective pins
    //--------------------------------------------------------------------------
    _LATD4      = 0;
    _LATD5      = 0;
    _LATD6      = 0;
    _LATD7      = 0;
	//--------------------------------------------------------------------------
	// Associate respective pins with OC1...OC4
	//--------------------------------------------------------------------------
	_RP68R		= 0b010000;	// OC1 on RP68/RD4
	_RP69R		= 0b010001;	// OC2 on RP68/RD4
	_RP70R		= 0b010010;	// OC3 on RP68/RD4
	_RP71R		= 0b010011;	// OC4 on RP68/RD4
    }
//==============================================================================
#endif      // __MCMProfile_H
