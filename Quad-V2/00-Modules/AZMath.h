//**********************************************************************
 float static inline	AZTanH(float arg)
	{
	if (arg < -3.0) return -1.0;
	if (arg >  3.0) return  1.0;
	//--------------------------
	float argSq	= arg*arg;
	return arg * ( 27.0 + argSq ) / ( 27.0 + 9.0 * argSq );
	}
